    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String user = username.getSelectedItem().toString();
        String pass = mdp1.getText();
        if (user.trim().isEmpty() || pass.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires", "Erreur", JOptionPane.ERROR_MESSAGE);
        } else {
            Employe employe = new Employe(pass, user);
            if (controleur.isLogin(employe)) {
                if (controleur.isStaff(employe)) {
                    MainForm main = new MainForm(user);
                    super.dispose();
                    main.setVisible(true);
                } else {
//                    JOptionPane.showMessageDialog(this, "attend votre formulaire", "Erreur", JOptionPane.ERROR_MESSAGE);
                    super.dispose();
                    new Acceuil(user).setVisible(true);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Vous n'etes pas l'employe", "Erreur", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

